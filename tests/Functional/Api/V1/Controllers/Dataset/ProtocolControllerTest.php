<?php

namespace Functional\Api\V1\Controllers\Dataset;

use App\Models\Dataset;
use App\Models\Project;
use App\Models\Protocol;
use App\Models\Team;
use App\Models\User;
use App\TestCase;
use Illuminate\Foundation\Testing\RefreshDatabase;
use Illuminate\Http\UploadedFile;

class ProtocolControllerTest extends TestCase
{
    use RefreshDatabase;

    const EMAIL = 'user@email.com';
    const PASSWORD = 'secret';

    /** @var Team */
    private $team;

    /** @var Project */
    private $project;

    /** @var Dataset A dataset in a project */
    private $dataset;

    /** @var Protocol */
    private $protocol;

    protected function setUp()
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        $user = factory(User::class)->create([
            'name' => 'Basic User',
            'email' => self::EMAIL,
            'password' => self::PASSWORD
        ]);

        $this->team = factory(Team::class)->create([
            'name' => 'Laboratoire de tests fonctionnels sur les remote directories'
        ]);

        $this->team->admins()->save($user);

        $projectManager = app(\App\Services\Projects\ProjectManager::class);

        $this->project = $projectManager->create($this->team, $user, [
            'name' => 'Dataset testing project',
        ]);

        $this->team->projects()->save($this->project);

        $this->login(self::EMAIL, self::PASSWORD);

        $datasetId = $this->post(sprintf('api/datasets/%s/upload', $this->project->directory->id), [
            'file' => UploadedFile::fake()->create('test.txt')
        ])
            ->assertStatus(201)
            ->assertJsonFragment([
                'name' => 'test.txt',
                'type' => 'dataset'
            ])
            ->json('id');

        $this->dataset = Dataset::find($datasetId);

        $this->protocol = factory(Protocol::class)->create([
            'user_id' => $user->id,
            'name' => 'Semaphore protocol'
        ]);
    }

    /**
     * @group RV-82
     */
    public function testAvailableAutocomplete()
    {
        $this->login();

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, 'phore'))
            ->assertStatus(200)
            ->assertJsonCount(1)
            ->assertJsonFragment([
                'name' => 'Semaphore protocol'
            ]);
    }

    /**
     * @group RV-82
     */
    public function testAvailableAutocompleteDoesntMatchQuery()
    {
        $this->login();

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, 'should not match anything'))
            ->assertStatus(200)
            ->assertJsonCount(0);
    }

    /**
     * @group RV-82
     */
    public function testAvailableAutocompleteDontFindFromOtherUsers()
    {
        // Random created user
        factory(Protocol::class)->create([
            'name' => 'Éléphant zèbre'
        ]);

        $this->login();

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, ''))
            ->assertStatus(200)
            ->assertJsonCount(1)
            ->assertJsonFragment([
                'name' => 'Semaphore protocol'
            ]);

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, 'zèb'))
            ->assertStatus(200)
            ->assertJsonCount(0);
    }

    /**
     * @group RV-82
     */
    public function testAvailableAutocompleteDontFindAlreadyAttached()
    {
        $this->login();

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, ''))
            ->assertStatus(200)
            ->assertJsonCount(1)
            ->assertJsonFragment([
                'name' => 'Semaphore protocol'
            ]);

        $this->put(sprintf('api/datasets/%s/protocols/%s', $this->dataset->id, $this->protocol->id))
            ->assertStatus(200);

        $this->get(sprintf('api/datasets/%s/protocols-available?query=%s', $this->dataset->id, ''))
            ->assertStatus(200)
            ->assertJsonCount(0);
    }

    /**
     * @group RV-82
     */
    public function testUpdate()
    {
        $this->login();

        $this->assertEquals(0, $this->dataset->protocols->count());

        $this->put(sprintf('api/datasets/%s/protocols/%s', $this->dataset->id, $this->protocol->id))
            ->assertStatus(200);

        $this->dataset->refresh();
        $this->assertEquals(1, $this->dataset->protocols->count());
    }

    /**
     * @group RV-82
     */
    public function testUpdateDontAllowAttachingProtocolsWeDontHaveAccessTo()
    {
        $protocol = factory(Protocol::class)->create();
        $this->login();

        $this->assertEquals(0, $this->dataset->protocols->count());

        $this->put(sprintf('api/datasets/%s/protocols/%s', $this->dataset->id, $protocol->id))
            ->assertStatus(403);
    }

    /**
     * @group RV-18
     */
    public function testStore()
    {
        $this->login();

        $this->assertDatabaseMissing('protocols', [
            'name' => 'My test protocol'
        ]);

        $this->post(sprintf('api/datasets/%s/protocols', $this->dataset->id), [
            'name' => 'My test protocol',
            'content' => 'My protocol text content'
        ])
            ->assertStatus(201)
            ->json('id');

        $this->assertDatabaseHas('protocols', [
            'name' => 'My test protocol'
        ]);
    }

    /**
     * @group RV-18
     */
    public function testDestroy()
    {
        $this->login();

        $protocolId = $this->post(sprintf('api/datasets/%s/protocols', $this->dataset->id), [
            'name' => 'My test protocol',
            'content' => 'My protocol text content'
        ])
            ->assertStatus(201)
            ->json('id');

        $this->assertDatabaseHas('dataset_protocol', [
            'dataset_id' => $this->dataset->id,
            'protocol_id' => $protocolId,
        ]);

        $this->delete(sprintf('api/datasets/%s/protocols/%s', $this->dataset->id, $protocolId))
            ->assertStatus(200);

        $this->assertDatabaseMissing('dataset_protocol', [
            'dataset_id' => $this->dataset->id,
            'protocol_id' => $protocolId,
        ]);
    }

    /**
     * @group RV-18
     */
    public function testShow()
    {
        $this->login();

        $protocolId = $this->post(sprintf('api/datasets/%s/protocols', $this->dataset->id), [
            'name' => 'My test protocol',
            'content' => 'My protocol text content'
        ])
            ->assertStatus(201)
            ->json('id');

        $this->get(sprintf('api/datasets/%s/protocols/%s', $this->dataset->id, $protocolId))
            ->assertStatus(200)
            ->assertJsonStructure([
                'id',
                'name',
                'content'
            ]);
    }
}
